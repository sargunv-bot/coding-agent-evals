export const glossary = [
  ['Provider','The service or endpoint that supplies access to a model.'],
  ['Model','The named AI system evaluated through a provider.'],
  ['Task','A reproducible software change request with a pinned repository state and deterministic verifier.'],
  ['Run','One model attempt at one task configuration.'],
  ['Run ID','The unique execution identifier assigned by the evaluation harness.'],
  ['Cell','A provider, model, task, mode, scenario, and repeat combination in an evaluation matrix.'],
  ['Experiment','A manifest that planned a group of cells; here it is provenance and filter metadata, not the primary navigation.'],
  ['Baseline mode','The model receives the task instruction without an interactive clarification channel.'],
  ['Ask user mode','The model may ask controlled clarification questions during the run.'],
  ['Full info mode','A diagnostic mode that supplies additional task information; it is not directly comparable to baseline.'],
  ['Scenario','A named variant of a task with distinct expected behavior.'],
  ['Repeat','The ordinal repetition of the same cell configuration.'],
  ['Deterministic result','The pass/fail outcome from executable verification. It is authoritative.'],
  ['Infrastructure error','A harness, environment, or provider failure that prevents a valid task result.'],
  ['Input tokens','Tokens sent to the model, excluding cached input when reported separately.'],
  ['Cached input tokens','Previously processed input reused by the provider.'],
  ['Output tokens','Tokens generated in the model response.'],
  ['Reasoning tokens','Provider-reported internal reasoning tokens, when available.'],
  ['Subjective review','A separate model-blind assessment of quality or mergibility; it can never override deterministic verification.'],
] as const;
